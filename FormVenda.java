    private void btConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarActionPerformed
                                           
        String cpf = txtCpfCli.getText().trim();

        if(cpf.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite o CPF do Cliente para buscar.", "Erro", 0);
            return;
        }

        Venda v = bd.consVendaPorCliente(cpf);
        
        if(v != null){
            txtCpfCli.setText(v.getCliente().getCpf());
            txtNomeFunc.setText(v.getFuncionario().getNome());
            txtNomeProd.setText(v.getProduto().getNome());
            txtQtd.setText(String.valueOf(v.getQuantidade()));
            
            JOptionPane.showMessageDialog(null, "Venda encontrada!", "Sucesso", 1);
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma venda encontrada para este CPF.", "Aviso", 2);
            limpar();
        }
            // TODO add your handling code here:
    }//GEN-LAST:event_btConsultarActionPerformed

