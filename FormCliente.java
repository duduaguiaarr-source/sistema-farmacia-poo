    private void btConsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsActionPerformed

    String cpfProcurado = txtCpf.getText().trim();
    
    if (cpfProcurado.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Digite um CPF para consultar.", "Alerta", 2);
        return;
    }

    Cliente c = bd.consClienteCpf(cpfProcurado);

    if (c != null) {
        txtCpf.setText(c.getCpf());
        txtNome.setText(c.getNome());
        txtEnd.setText(c.getEndereco());
        txtTel.setText(c.getTelefone());
        
        javax.swing.JOptionPane.showMessageDialog(null, "Cliente encontrado: " + c.getNome(), "Sucesso", 1);
    } else {
        javax.swing.JOptionPane.showMessageDialog(null, "Cliente não encontrado.", "Erro", 0);
    }
                                                              // TODO add your handling code here:
    }//GEN-LAST:event_btConsActionPerformed

    private void btAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAltActionPerformed

    String cpfParaAlterar = txtCpf.getText().trim();
    
    if (cpfParaAlterar.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Digite o CPF do cliente que deseja alterar.", "Alerta", 2);
        return;
    }

    Cliente c = new Cliente();
    c.setCpf(cpfParaAlterar);

    Cliente atualizado = bd.altCliente(c);

    if (atualizado != null) {
        txtNome.setText(atualizado.getNome());
        txtEnd.setText(atualizado.getEndereco());
        txtTel.setText(atualizado.getTelefone());
        
        javax.swing.JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!", "Alteração", 1);
    } else {
        javax.swing.JOptionPane.showMessageDialog(null, "CPF não encontrado.", "Erro", 0);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btAltActionPerformed

    private void btDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDelActionPerformed

    String cpfParaExcluir = txtCpf.getText().trim();
    
    if (cpfParaExcluir.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Digite o CPF para excluir.", "Alerta", 2);
        return;
    }

    int confirma = javax.swing.JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?", "Atenção", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirma == javax.swing.JOptionPane.YES_OPTION) {
        Cliente removido = bd.delCliente(cpfParaExcluir);

        if (removido != null) {
            javax.swing.JOptionPane.showMessageDialog(null, "Cliente removido: " + removido.getNome(), "Exclusão", 1);
            limpar();
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "CPF não encontrado no sistema.", "Erro", 0);
        }
    }
                               // TODO add your handling code here:
    }//GEN-LAST:event_btDelActionPerformed

