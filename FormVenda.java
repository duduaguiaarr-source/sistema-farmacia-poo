    private void btInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirActionPerformed

    if(txtCpfCli.getText().isEmpty() || txtNomeProd.getText().isEmpty() || 
       txtNomeFunc.getText().isEmpty() || txtQtd.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Erro", 0);
        return;
    }

    Cliente c = bd.consClienteCpf(txtCpfCli.getText().trim());
    Funcionario f = bd.consFuncNome(txtNomeFunc.getText().trim());
    Produto p = bd.consProdutoNome(txtNomeProd.getText().trim());

    if(c == null){
        JOptionPane.showMessageDialog(null, "Cliente não encontrado (CPF).", "Erro", 0);
        return;
    }
    if(f == null){
        JOptionPane.showMessageDialog(null, "Funcionário não encontrado.", "Erro", 0);
        return;
    }
    if(p == null){
        JOptionPane.showMessageDialog(null, "Produto não encontrado.", "Erro", 0);
        return;
    }

    try {
        int qtd = Integer.parseInt(txtQtd.getText().trim());

        Venda v = new Venda();
        v.setCliente(c);
        v.setFuncionario(f);
        v.setProduto(p);
        v.setQuantidade(qtd);
        v.setDataVenda(java.time.LocalDate.now());

        bd.addVenda(v); 

        JOptionPane.showMessageDialog(null, "Venda registrada com sucesso!", "Sucesso", 1);
        limpar();

    } 
    catch (EstoqueException ex) { 
        JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro de Estoque", 0);
    } 
    catch (VendaException ex) { 
        JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro na Venda", 0);
    }
    catch (NumberFormatException e){ 
        JOptionPane.showMessageDialog(null, "Quantidade inválida! Digite apenas números.", "Erro", 0);
    }
          // TODO add your handling code here:
    }//GEN-LAST:event_btInserirActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        String cpf = txtCpfCli.getText().trim();
        if(cpf.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite o CPF do Cliente para excluir.", "Erro", 0);
            return;
        }
        
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza? O estoque será devolvido.", "Atenção", JOptionPane.YES_NO_OPTION);
        
        if(confirma == JOptionPane.YES_OPTION){
            if(bd.delVenda(cpf) != null){
                 JOptionPane.showMessageDialog(null, "Venda cancelada e estoque reposto!", "Sucesso", 1);
                 limpar();
            } else {
                 JOptionPane.showMessageDialog(null, "Venda não encontrada.", "Erro", 0);
            }
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

