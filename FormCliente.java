    private void btInsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsActionPerformed

    if (txtCpf.getText().trim().isEmpty() || txtNome.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Preencha pelo menos CPF e Nome!", "Alerta", 2);
        return; 
    }

    Cliente c = new Cliente();
    c.setCpf(txtCpf.getText().trim());
    c.setNome(txtNome.getText().trim());
    c.setEndereco(txtEnd.getText().trim());
    c.setTelefone(txtTel.getText().trim());

    if (bd.insCliente(c) != null) {
        javax.swing.JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!", "Sucesso", 1);
        limpar();
    } else {
        javax.swing.JOptionPane.showMessageDialog(null, "Já existe um cliente com este CPF!", "Erro", 0);
    }

    }//GEN-LAST:event_btInsActionPerformed

